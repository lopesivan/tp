# Run!
# edit last files
el() {
  local dir
  file=$(redis-cli ZRANGE vimmru 0 -1 | awk '{print $1}'|
        fzf-tmux -l 100% --multi --reverse --query="$1" --select-1 --exit-0)
  [ -n "$file" ] && vim $file
}
el $1

# ----------------------------------------------------------------------------
exit 0
