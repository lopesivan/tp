#!/bin/bash
# ----------------------------------------------------------------------------
# ONLY CHANGE THIS PART
GITORIUS=gitorious.dev.apptechcorp.com
PASTEBIN=$GITORIUS
    PORT=4080
# ----------------------------------------------------------------------------

##############################################################################
##############################################################################
##############################################################################

usage() {
	cat <<EOF
Usage  : `basename $0` id
Example: `basename $0` 4edf070ecaea1

http://${PASTEBIN}:${PORT}/spindle/paste/display/id/4edf0ce7ba622#active
EOF
	exit 1
}

[ "$1" ] || { usage; }

# ----------------------------------------------------------------------------
# ----------------------------------------------------------------------------
# ----------------------------------------------------------------------------

ID=$1
lynx -dump  http://${PASTEBIN}:${PORT}/spindle/paste/display/id/${ID}#${ID} | sed -n -e '/= @begin = cdb906ad-130f-4469-a085-7a114f2ae310 =/,/= @end   = cdb906ad-130f-4469-a085-7a114f2ae310 =/p' | sed '1d;$d'

# ----------------------------------------------------------------------------
exit 0
