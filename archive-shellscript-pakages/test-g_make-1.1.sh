#! /bin/bash
# s - simple
# m -multipla

case $1 in

	cpp-s)
#-----------------------------------------------------------------------------
rm *.cpp
for src in $(echo example-{1..10}.cpp| sed ":a;s/\([^0-9]\)\([0-9]\{1,2\}\.\)/\10\2/;ta");
do
	hw -C > $src
done

ls *.cpp| ./g_mk-cpp-1.1.sh > Makefile
#-----------------------------------------------------------------------------
;;

	c-s)
#-----------------------------------------------------------------------------
rm *.c
for src in $(echo example-{1..10}.c| sed ":a;s/\([^0-9]\)\([0-9]\{1,2\}\.\)/\10\2/;ta");
do
	hw -c > $src
done

ls *.c| ./g_mk-c-1.1.sh > Makefile
#-----------------------------------------------------------------------------
;;

	tex-s)
#-----------------------------------------------------------------------------

rm *.tex
for src in $(echo example-{1..10}.tex| sed ":a;s/\([^0-9]\)\([0-9]\{1,2\}\.\)/\10\2/;ta");
do
	hw -t > $src
done

ls *.tex| ./g_mk-tex-1.1.sh > Makefile
#-----------------------------------------------------------------------------
;;

	c-m)
#-----------------------------------------------------------------------------
rm *.c
./g_c_prj.1.1.sh

ls *.c| ./g_mk-c-1.2.sh > Makefile
#-----------------------------------------------------------------------------

;;
	*) rm *.tex *.c *.cpp *.o *.h
;;
esac
